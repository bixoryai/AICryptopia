// Auto-play functionality (optional)
let autoPlayInterval;
let isAutoPlaying = false;

function toggleAutoPlay() {
    if (isAutoPlaying) {
        stopAutoPlay();
    } else {
        startAutoPlay();
    }
}

function startAutoPlay() {
    isAutoPlaying = true;
    autoPlayInterval = setInterval(() => {
        if (currentSlide < totalSlides) {
            changeSlide(1);
        } else {
            stopAutoPlay();
        }
    }, 5000); // 5 seconds per slide
}

function stopAutoPlay() {
    isAutoPlaying = false;
    if (autoPlayInterval) {
        clearInterval(autoPlayInterval);
    }
}

// Utility Functions
function goToSlide(slideNumber) {
    if (slideNumber >= 1 && slideNumber <= totalSlides) {
        currentSlide = slideNumber;
        showSlide(currentSlide);
        updateProgressBar();
        updateSlideCounter();
        updateNavigationState();
        initializeSlideCharts(currentSlide);
        
        // Auto-close sidebar after navigation (helpful for mobile)
        if (sidebarOpen && window.innerWidth <= 768) {
            setTimeout(() => closeSidebar(), 300);
        }
    }
}

// Enhanced Navigation Functions
function goToFirstSlide() {
    currentSlide = 1;
    showSlide(currentSlide);
    updateProgressBar();
    updateSlideCounter();
    updateNavigationState();
    initializeSlideCharts(currentSlide);
}

function goToLastSlide() {
    currentSlide = totalSlides;
    showSlide(currentSlide);
    updateProgressBar();
    updateSlideCounter();
    updateNavigationState();
    initializeSlideCharts(currentSlide);
}

// Update navigation button states
function updateNavigationState() {
    const homeBtn = document.querySelector('.home-btn');
    const prevBtn = document.querySelector('.prev-btn');
    const nextBtn = document.querySelector('.next-btn');
    const endBtn = document.querySelector('.end-btn');
    
    // Highlight home button if on first slide
    if (currentSlide === 1) {
        homeBtn.classList.add('active');
    } else {
        homeBtn.classList.remove('active');
    }
    
    // Highlight end button if on last slide
    if (currentSlide === totalSlides) {
        endBtn.classList.add('active');
    } else {
        endBtn.classList.remove('active');
    }
    
    // Disable prev button on first slide
    if (currentSlide === 1) {
        prevBtn.classList.add('disabled');
    } else {
        prevBtn.classList.remove('disabled');
    }
    
    // Disable next button on last slide
    if (currentSlide === totalSlides) {
        nextBtn.classList.add('disabled');
    } else {
        nextBtn.classList.remove('disabled');
    }
    
    // Update slide indicators and sidebar
    updateSlideIndicators();
    updateSidebarIndex();
}

// Sidebar Functionality
let sidebarOpen = false;

function toggleSidebar() {
    const sidebar = document.getElementById('slide-sidebar');
    const overlay = document.getElementById('sidebar-overlay');
    const toggleBtn = document.querySelector('.sidebar-toggle');
    
    sidebarOpen = !sidebarOpen;
    
    if (sidebarOpen) {
        sidebar.classList.add('open');
        overlay.classList.add('active');
        toggleBtn.classList.add('active');
        toggleBtn.innerHTML = '<i class="fas fa-times"></i>';
    } else {
        sidebar.classList.remove('open');
        overlay.classList.remove('active');
        toggleBtn.classList.remove('active');
        toggleBtn.innerHTML = '<i class="fas fa-bars"></i>';
    }
}

function closeSidebar() {
    if (sidebarOpen) {
        toggleSidebar();
    }
}

function updateSidebarIndex() {
    const indexItems = document.querySelectorAll('.index-item');
    indexItems.forEach((item, index) => {
        if (index + 1 === currentSlide) {
            item.classList.add('active');
        } else {
            item.classList.remove('active');
        }
    });
}

// Touch/Swipe Support for Mobile
let touchStartX = 0;
let touchEndX = 0;

document.addEventListener('touchstart', function(event) {
    touchStartX = event.changedTouches[0].screenX;
});

document.addEventListener('touchend', function(event) {
    touchEndX = event.changedTouches[0].screenX;
    handleSwipe();
});

function handleSwipe() {
    const swipeThreshold = 50;
    const diff = touchStartX - touchEndX;
    
    if (Math.abs(diff) > swipeThreshold) {
        if (diff > 0) {
            // Swipe left - next slide
            changeSlide(1);
        } else {
            // Swipe right - previous slide
            changeSlide(-1);
        }
    }
}

